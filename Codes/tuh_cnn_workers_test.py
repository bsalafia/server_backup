class TrainGenerator(keras.utils.Sequence):
    # Class is a dataset wrapper for better training performance
    def __init__(self, x,x_one,x_zero,y_one,y_zero, batch_size):

      self.x_one, self.y_one = x_one,y_one
      self.x=x
      self.x_zero, self.y_zero = x_zero,y_zero
      self.batch_size = batch_size
      self.mini_batch=int(batch_size/2)
      self.indices_one = np.arange(self.x_one.shape[0])
      self.indices_zero = np.arange(self.x_zero.shape[0])

    def __len__(self):

      return math.floor(self.x.shape[0] / self.batch_size)

    def __getitem__(self, idx):
    
      inds_one = self.indices_one[idx * self.mini_batch:(idx + 1) * self.mini_batch]
      inds_zero = self.indices_zero[idx * self.mini_batch:(idx + 1) * self.mini_batch]
      batch_x_one = self.x_one[inds_one]
      batch_x_zero = self.x_zero[inds_zero]
      batch_y_one = self.y_one[inds_one]
      batch_y_zero = self.y_zero[inds_zero]
      batch_x=np.concatenate((batch_x_zero, batch_x_one), axis=0)
      batch_y=np.concatenate((batch_y_zero, batch_y_one), axis=0)
      return batch_x, batch_y
    
    def on_epoch_end(self):

      np.random.shuffle(self.indices_one)
      np.random.shuffle(self.indices_zero)

class ValidationGenerator(Sequence):

    # Class is a dataset wrapper for better training performance
  def __init__(self, x_set, y_set, batch_size):

    self.x, self.y = x_set, y_set
    self.sh_x=x_set.shape[0]
    self.batch_size = batch_size
    self.indices = np.arange(self.sh_x)

  def __len__(self):
    return math.ceil(self.sh_x / self.batch_size)

  def __getitem__(self, idx):
    inds = self.indices[idx * self.batch_size:(idx + 1) * self.batch_size]

    yield self.x[inds], self.y[inds]
    
  def on_epoch_end(self):

    np.random.shuffle(self.indices)

